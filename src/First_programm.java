public class First_programm {
    public static void main(String[] args) {
        //Addition
        int a=12;
       int b=15;
        System.out.println(a+b);

        //subtraction
        float x=25.5f,y=5.4f;
        System.out.println(x-y);

        //Multiplication
        double p=12d,q=23d;
        System.out.println(p*q);

       //modulous
        long _a=234567l,_b=16789l;
        System.out.println(_a%_b);

        //divide
        short _p=23453 ,_q=31234;
        System.out.println(_p/_q);

        //Char
        char result='A';
        System.out.println("my result is " + result +   "  grade");

        //Boolean
        boolean right=true;
        boolean wrong=false;
        System.out.println("I am right -->" + right);
        System.out.println("I am wrong -->" + wrong);

        //byte ---128 to 127
        byte i=24,j=45;
        System.out.println(i<j);

       //LOGICAL OPERATORS
        System.out.println(a<25 && a<34);
        System.out.println(!(a<25 && a<34));
        System.out.println(a<25 || a>34);
        System.out.println(a>25 || a>34);







    }
}
